package salesAnalysis;
public class Discount {
	int totalCnt;
	int noDiscount;
	int disable;
	int merit;
	int multichild;
	int pregnant;
	
	public Discount(int totalCnt, int noDiscount, int disable, int merit, int multichild, int pregnant) {
		this.totalCnt = totalCnt;
		this.noDiscount = noDiscount;
		this.disable = disable;
		this.merit = merit;
		this.multichild = multichild;
		this.pregnant = pregnant;
	}
	
}
